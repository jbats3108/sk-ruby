weather = 'humid'

case weather
when 'wet'
  puts "Wear a raincoat"
when 'sunny'
  puts "Don't forget your shades!"
when 'cloudy'
  puts "Boring, is it at least warm?"
when 'cold'
  puts "Brr, I need a jacket"
when 'foggy'
  puts "Remember your fog lights"
when 'humid'
  puts "Make a F.R.I.E.N.D.S reference"
else
  puts "Is there any weather then??"
end
